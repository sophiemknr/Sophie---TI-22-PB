#include <iostream>
using namespace std;

int main()
{
  int bil;

  cout << "Program Perulangan Ascending\n";
  for (bil = 1; bil <=10; bil++)
    cout << bil << "\n";
cout<<"\n";
  cout << "Program Perulangan Descending\n";
  for (bil = 10; bil >=1; bil--)
    cout << bil<< "\n";
  getchar();
}